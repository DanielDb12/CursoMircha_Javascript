// objeto math

console.log(Math);
console.log(Math.PI);
// absoluto es la cantidad sin considerar quee es positivo o negativo
console.log(Math.abs(-7.8));
// ceil redondea al numero entero posterior inmediato
console.log(Math.ceil(7.2));
// floor baja redondea inmediato entero menor
console.log(Math.floor(7.8));
// round redondea al mas cercano
console.log(Math.round(7.49));
// sqrt para raiz cudadrada
console.log(Math.sqrt(81));
// pow potencia
console.log(Math.pow(2.5));
// sign es un indicador del numero si es negativo 0 devuelve 0 
console.log(Math.sign(0));
// random da numeros aleatorios
console.log(Math.round(Math.random()*1000))